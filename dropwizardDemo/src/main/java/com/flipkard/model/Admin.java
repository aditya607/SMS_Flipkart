package com.flipkard.model;

public class Admin {
	private String AdminName;
	private String AdminAddress;
	private String AdminMobileNO;
	private String AdminEmail;
	private String AdminRoomNo;
	
	
	
	
	
	
	public String getAdminName() {
		return AdminName;
	}
	public void setAdminName(String adminName) {
		AdminName = adminName;
	}
	public String getAdminAddress() {
		return AdminAddress;
	}
	public void setAdminAddress(String adminAddress) {
		AdminAddress = adminAddress;
	}
	public String getAdminMobileNO() {
		return AdminMobileNO;
	}
	public void setAdminMobileNO(String adminMobileNO) {
		AdminMobileNO = adminMobileNO;
	}
	public String getAdminEmail() {
		return AdminEmail;
	}
	public void setAdminEmail(String adminEmail) {
		AdminEmail = adminEmail;
	}
	public String getAdminRoomNo() {
		return AdminRoomNo;
	}
	public void setAdminRoomNo(String adminRoomNo) {
		AdminRoomNo = adminRoomNo;
	}
	
}
